package com.store.utils;

import java.util.Date;
import java.util.Properties;

import javax.mail.Message;
import javax.mail.Session;
import javax.mail.Transport;
import javax.mail.internet.InternetAddress;
import javax.mail.internet.MimeMessage;

public class MailUtil {
	public static void sendMail(String fromMail,String user,String password,
			String toMail,String mailTitle,String mailContent) throws Exception{
		Properties properties = new Properties();
		
		//使用smtp:简单邮件传输协议
		
		//存储发送邮件服务器的信息  
		properties.put("mail.smtp.host", "smtp.163.com");
		//同时通过验证
		properties.put("mail.smtp.auth", "true");
		
		properties.setProperty("mail.debug", "true");
		properties.setProperty("mail.transport.protocol", "smtp");
		
		//根据属性新建一个邮件会话  
		Session session = Session.getInstance(properties);
		session.setDebug(true); //有他会打印一些调试信息。  
		
		//由邮件会话新建一个消息对象  
		MimeMessage message = new MimeMessage(session);
		
		//设置发件人的地址  
		message.setFrom(new InternetAddress(fromMail));
		
		//设置收件人,并设置其接收类型为TO  
		message.setRecipient(Message.RecipientType.TO, new InternetAddress(toMail));
		
		//设置标题  
		message.setSubject(mailTitle);
		
		//设置信件内容  
		//message.setText(mailContent); //发送 纯文本 邮件 todo  
		
		//发送HTML邮件，内容样式比较丰富  
		message.setContent(mailContent, "text/html;charset=gbk"); 
		message.setSentDate(new Date());//设置发信时间  
		message.saveChanges();//存储邮件信息  
		
		//发送邮件  
		Transport transport = session.getTransport("smtp");  
		//Transport transport = session.getTransport();  
		transport.connect(user, password);  
		transport.sendMessage(message, message.getAllRecipients());//发送邮件,其中第二个参数是所有已设好的收件人地址  
		transport.close();  
		
	}
	
	public static void main(String[] args) throws Exception {
		sendMail("swancuikqq635@163.com",
				"swancuikqq635", "a19881023", "694740885@qq.com", "激活邮件", "<a href='http://localhost/Store/index'>http://localhost/Store/index</a>");
	}
}
